 package com.tnsif.basics;

public class TypeconversionNarrowing {
	public static void main(String[] args) {
		
	
	double myDouble = 9.78d;//double value
	int myInt = (int) myDouble;//it converts double value into int
	System.out.println(myDouble);//prints double value
	System.out.println(myInt);//prints int value
	
	}
}